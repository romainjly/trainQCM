var score = 0;

function iteration(id)
{
    if (selectedButton == "true") {
        score++;
        console.log("le score : " + score);
    } else if (selectedButton == "false") {
        score-= 0.5;
        console.log("le score : " + score);
    }

    var q = 0;
    q = document.getElementById('question');
    q.innerText = Questions[id].question;

    // get buttons options
    const choice1 = document.getElementById('choice1');
    const choice2 = document.getElementById('choice2');
    const choice3 = document.getElementById('choice3');
    const choice4 = document.getElementById('choice4');

    // get buttons text
    choice1.innerText = Questions[id].answers[0].text;
    choice2.innerText = Questions[id].answers[1].text;
    choice3.innerText = Questions[id].answers[2].text;
    choice4.innerText = Questions[id].answers[3].text;

    // get buttons value
    choice1.value = Questions[id].answers[0].isCorrect;
    choice2.value = Questions[id].answers[1].isCorrect;
    choice3.value = Questions[id].answers[2].isCorrect;
    choice4.value = Questions[id].answers[3].isCorrect;

    // reset button color
    choice1.style.background = 'white';
    choice2.style.background = 'white';
    choice3.style.background = 'white';
    choice4.style.background = 'white';

    /*--------------------------- Click event ------------------------------*/

    choice1.addEventListener('click', () => {
        choice1.style.background = 'lightgrey';
        choice2.style.background = 'white';
        choice3.style.background = 'white';
        choice4.style.background = 'white';
        selectedButton = choice1.value;
    })
    choice2.addEventListener('click', () => {
        choice2.style.background = 'lightgrey';
        choice1.style.background = 'white';
        choice3.style.background = 'white';
        choice4.style.background = 'white';
        selectedButton = choice2.value;
    })
    choice3.addEventListener('click', () => {
        choice3.style.background = 'lightgrey';
        choice1.style.background = 'white';
        choice2.style.background = 'white';
        choice4.style.background = 'white';
        selectedButton = choice3.value;
    })
    choice4.addEventListener('click', () => {
        choice4.style.background = 'lightgrey';
        choice1.style.background = 'white';
        choice2.style.background = 'white';
        choice3.style.background = 'white';
        selectedButton = choice4.value;
    })
    /*-----------------------------------------------------------------------*/

    console.log(selectedButton)
    console.log("fin")

    selectedButton = "";
    console.log('score is ' + score);
}

